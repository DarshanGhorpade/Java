
// 7. Write a program to print 1 to 10 using the do-while loop statement.

class question7{

	public static void main(String[] args){
		int i = 1;
		System.out.print("Numbers from 1 to 10 : ");
		do{
			System.out.print(i + " ");
			i++;
		}while(i < 11);
	}// End main

}// End question7 class


// 4. Define variables of different Data Types int, Boolean, char, float, double and print on the Console.

class question4{

	public static void main(String[] args){

		int a = 10;
		boolean b = true;
		char ch = 'A';
		float pi = 3.14f;
		double d = 10.11d;

		System.out.println("interger a = "+a);
		System.out.println("boolean b = "+b);
		System.out.println("char ch = "+ch);
		System.out.println("float pi = "+pi);
		System.out.println("double d = "+d);
		
	}// End main

}// End question4 class
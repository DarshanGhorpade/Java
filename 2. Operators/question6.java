

// 6. Program for relational operators (<,<==, >, >==)

class question6{

	public static void main(String[] args){

		//Variables Declaration and Initialization
		int num1 = 12, num2 = 4;

		//Less than
		System.out.println("num1 <  num2 = " + (num1 <  num2) );

		//Less than or equal to
		System.out.println("num1 <= num2 = " + (num1 <= num2) );

		//Greater than
		System.out.println("num1 >  num2 = " + (num1 >  num2) );

		//Greater than or equal to
		System.out.println("num1 >= num2 = " + (num1 >= num2) );

	}// End main

}// End question6 class